// Regenerates supabase/seed.sql from src/data/seed-data.json so the SQL seed
// and the local-adapter seed can never drift. Run: node scripts/gen-seed-sql.mjs
import fs from 'node:fs'
import path from 'node:path'
import crypto from 'node:crypto'

const root = path.resolve(import.meta.dirname, '..')
const seed = JSON.parse(fs.readFileSync(path.join(root, 'src/data/seed-data.json'), 'utf8'))

// Deterministic uuid per seed key, so reruns produce identical SQL.
function uuidFor(key) {
  const h = crypto.createHash('md5').update(`planner-seed:${key}`).digest('hex')
  return `${h.slice(0, 8)}-${h.slice(8, 12)}-${h.slice(12, 16)}-${h.slice(16, 20)}-${h.slice(20, 32)}`
}

const q = (s) => (s == null ? 'null' : `'${String(s).replaceAll("'", "''")}'`)
const futureDate = (d) => (d == null ? 'null' : `(current_date + ${d})`)
const pastTs = (d) => `(now() - interval '${d} days')`

let sql = `-- Generated by scripts/gen-seed-sql.mjs from src/data/seed-data.json.
-- Do not edit by hand; edit the JSON and rerun the script.

`

for (const p of seed.projects) {
  sql +=
    `insert into projects (id, name, area, goal, target_date, created_at) values\n` +
    `  (${q(uuidFor(p.key))}, ${q(p.name)}, ${q(p.area)}, ${q(p.goal)}, ${futureDate(p.targetInDays)}, ${pastTs(50)});\n`
}

sql += '\n'

for (const i of seed.items) {
  const deps = (i.dependsOnKeys ?? []).map((k) => q(uuidFor(k))).join(', ')
  sql +=
    `insert into items (id, title, notes, area, project_id, section, assignee, effort, hard_deadline, importance, depends_on, status, created_at, last_touched_at, last_touch_note) values\n` +
    `  (${q(uuidFor(i.key))}, ${q(i.title)}, ${q(i.notes)}, ${q(i.area)}, ` +
    `${i.projectKey ? q(uuidFor(i.projectKey)) : 'null'}, ${q(i.section)}, ${q(i.assignee)}, ` +
    `${q(i.effort)}, ${futureDate(i.deadlineInDays)}, ${i.importance}, ` +
    `array[${deps}]::uuid[], ${q(i.status ?? 'open')}, ` +
    `${pastTs(i.createdDaysAgo)}, ${pastTs(i.touchedDaysAgo)}, ${q(i.lastTouchNote)});\n`
}

fs.writeFileSync(path.join(root, 'supabase/seed.sql'), sql)
console.log(`wrote supabase/seed.sql (${seed.projects.length} projects, ${seed.items.length} items)`)
